    private void pnMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnMouseMoved
        if (isAktif()) {
            pn_select.setBackground(getaSelect());
            if (pn.getBackground() != getaBacground3()) {
                pn.setBackground(getaBacground2());
            }
        }
    }//GEN-LAST:event_pnMouseMoved

    private void pnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnMouseClicked
        if (isAktif()) {
            tekan();
        }
    }//GEN-LAST:event_pnMouseClicked

    private void pnMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnMouseExited
        if (isAktif()) {
            pn_select.setBackground(getaBacground3());
            if (pn.getBackground() != getaBacground3()) {
                pn.setBackground(getaBacground1());
                pn_select.setBackground(getaBacground1());
            }
        }
    }//GEN-LAST:event_pnMouseExited


