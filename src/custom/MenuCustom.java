    public void reset() {
        pn.setBackground(getaBacground1());
        pn_select.setBackground(getaBacground1());
        lb_menu.setFont(getaFont1());
        lb_menu.setText(getaLebel());
        lb_icon.setIcon(getaIcon());
    }

    public void tekan() {
        lb_menu.setFont(getaFont2());
        pn.setBackground(getaBacground3());
        act.actionPerformed(null);
    }

